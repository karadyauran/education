# upper case
a = "Hello, World!"
print(a.upper())

print('-------------------------')

# lower case
a = "Hello, World!"
print(a.lower())

print('-------------------------')

a = " Hello, World! "
print(a.strip())  # returns "Hello, World!"

print('-------------------------')

a = "Hello, World!"
print(a.replace("H", "J"))
print(a.replace("o", "p"))  # replaces all characters in the string

print('-------------------------')

a = "Hello, World!"
print(a.split(","))  # returns ['Hello', ' World!']


